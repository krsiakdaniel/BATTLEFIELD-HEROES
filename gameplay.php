Gameplay is slightly different than you might be expecting. When you see the game&#39;s cartoony style it is easy to assume the gameplay will be fast.
</p>

<ul class="list-square">
<li>Very soon you will realise that the game is deliberately a bit slower than you were thinking.</li>
<li>First time you run with your hero it feels slow and clumsy. Once you engage your enemy it becomes clear it is not that bad idea after all.</li>
<li>Battlefield Heroes is dedicated for more casual players so it ensures that you do not need any special skills or long years of experience in <abbr title="* FIRST PERSON SHOOTER *">fps</abbr>.</li>
</ul>
