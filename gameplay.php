<a href="armies-national-army.php"><img src="images/build-pics/gameplay/army-n.png" width="320" height="180" alt="National Army" class="custom-div-blue float-l img-border-black" /></a>
<p>
Battlefiled heroes uses a mix between <abbr title="* ROLE-PLAYING GAME *">rpg</abbr> and ordinary <span class="text-uppercase">fps</span>.
While the very core
<br />
of this game is still good old shooting it mixes it with clever skill system. 
<br />
There is a bar with the different <a href="abilities.php">abilities</a> available for you at the top of your screen. Simply by pressing the corresponding button you can add &#47; reorganise your skill points.
There are various abilities available to all <a href="classes.php">classes</a>.
They include things like <a href="abilities-soldier.php#a-s" title="Soldier">Combat medicine</a>, <a href="abilities-gunner.php#a-g" title="Gunner">Leg it</a>, <a href="abilities-commando.php#a-c" title="Commando">Piercing shot</a> and many more alike !
</p>
