<p>
Once you know your hero you will pose great threat to your enemies !
</p>

<hr />

<p>
Battlefield Heroes uses familiar &#34;Capture the flag&#34; system we are used to from all previous Battlefield series games.
This system requires 2 teams fighting one against each other in order to control a certain number of control points.
<br />
Team which holds a majority of flags is winning slowly but surely because the other team will start losing its points.
The first team to lose all its points loses the game.
</p>
<p>Well do not be that sure with yourself now !</p>
<ul class="list-square">
<li>While you are capturing one flag you can hardly defend the rest of them.</li>
<li>This means that you have to think about offense or &#47; and defence all the time.</li>
<li>So it is really essential in order to win to cooperate with your team !</li>
</ul>
<p>This system makes the game interesting each time you play and there are many possible scenarios how you can win.</p>
